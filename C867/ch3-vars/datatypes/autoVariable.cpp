#include <iostream>
using namespace std;

int main() {
    // Let the compiler figure out what type everything is. Also allows modifiers like "const"
    const auto I = 100;    // int
    auto c = '2';    // char
    auto x = "hello";    // char*
    auto d = 2.32131256578;    // double
    auto f = 3.14;    // float
    
    return 0;
}
